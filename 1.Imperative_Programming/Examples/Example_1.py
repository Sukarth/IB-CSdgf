
a = 54
b = 28
print(a, b) # This will print them with a space in between
print('The difference is: ', a - b)
print(f'The ratio is {a / b}')
print(f'The ratio is {a // b}') # This will round down.
print(f'The remainder is {((3*a) % (2 * b) )}') # Modulus operator takes precedence; without brackets, a % 2 would occur first.